package Java;

public class Loops {
    public static void main(String[] args) {
        int count = 1;
        while (count != 6) {
            System.out.println(count);
            count++;
        }
        System.out.println();
        for (int i = 1; i != 6; i++) {
            System.out.println(i);

        }
    }
}
